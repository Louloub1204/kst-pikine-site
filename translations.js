const I18N = {
  // Langue courante (lue depuis localStorage ou défaut fr)
  current: localStorage.getItem('kst_lang') || 'fr',

  // Appliquer la langue à toute la page
  apply(lang) {
    this.current = lang;
    localStorage.setItem('kst_lang', lang);

    // Direction RTL pour l'arabe
    document.documentElement.lang = lang;
    document.documentElement.dir  = lang === 'ar' ? 'rtl' : 'ltr';
    if (lang === 'ar') {
      document.body.style.fontFamily = "'Amiri', 'Raleway', sans-serif";
    } else {
      document.body.style.fontFamily = "'Raleway', sans-serif";
    }

    // Traduire tous les éléments data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (TRANSLATIONS[key] && TRANSLATIONS[key][lang]) {
        el.textContent = TRANSLATIONS[key][lang];
      }
    });

    // Traduire les placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (TRANSLATIONS[key] && TRANSLATIONS[key][lang]) {
        el.placeholder = TRANSLATIONS[key][lang];
      }
    });

    // Marquer le bouton actif dans le sélecteur
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Mettre à jour la flèche des liens si RTL
    document.querySelectorAll('[data-i18n]').forEach(el => {
      let txt = el.textContent;
      if (lang === 'ar') {
        txt = txt.replace(/→/g, '←');
      } else {
        txt = txt.replace(/←/g, '→');
      }
      el.textContent = txt;
    });
  },

  // Initialiser au chargement
  init() {
    this.apply(this.current);
  }
};

// Auto-init
document.addEventListener('DOMContentLoaded', () => I18N.init());
